use std::fmt::{Debug, Display};

#[derive(PartialEq, Eq, Clone, Copy, Debug)]
pub enum Kind {
    Identifier,
    Number,
    Boolean,
    OpenBraces,
    CloseBraces,
    OpenParenthesis,
    CloseParenthesis,
    OpenBrackets,
    CloseBrackets,
    Plus,
    PlusPlus,
    PlusEquals,
    Minus,
    MinusMinus,
    MinusEquals,
    Slash,
    SlashEquals,
    Star,
    StarEquals,
    Mod,
    ModEquals,
    WhiteSpace,
    EndOfFile,
    Semicolon,
    Colon,
    Comma,
    Let,
    Const,
    Fun,
    If,
    Else,
    While,
    For,
    In,
    Return,
    Bad,
    Equals,
    EqualsEquals,
    Pipe,
    PipePipe,
    PipeEquals,
    Ampersand,
    AmpersandAmpersand,
    AmpersandEquals,
    LessThan,
    LessThanLessThan,
    LessThanEquals,
    GreaterThan,
    GreaterThanGreaterThan,
    GreaterThanEquals,
    Exclamation,
    ExclamationEquals,
    Circumflex,
    CircumflexEquals,
    Tilde,
    TildeEquals,
    UpTo,
    DownTo,
    Char,
    String,
}

impl Display for Kind {
    fn fmt(&self, f: &mut std::fmt::Formatter<'_>) -> std::fmt::Result {
        write!(f, "{:?}", self)
    }
}
